#include "math.hpp"
#include <iostream>

using namespace N;

double Math::add(double a, double b)
{
    return a + b;
}

double Math::remove(double a, double b)
{
    return a - b;
}

double Math::multi(double a, double b)
{
    return a * b;
}

double Math::divi(double a, double b)
{
    return a / b;
}